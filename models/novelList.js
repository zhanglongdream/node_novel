const mongoose = require('mongoose')

const novelListSchema = new mongoose.Schema({
  name: String,
  time: String,
  content: Object,
  list: String,
  img: String,
  meta: {
    createdAt: {
      type: Date,
      default: Date.now()
    },
    updatedAt: {
      type: Date,
      default: Date.now()
    }
  }
})

novelListSchema.pre('save', function (next) {
  if (this.isNew) {
    this.meta.createdAt = this.meta.updatedAt = Date.now()
  } else {
    this.meta.updatedAt = Date.now()
  }
  next()
})

module.exports = mongoose.model('Novel', novelListSchema)

